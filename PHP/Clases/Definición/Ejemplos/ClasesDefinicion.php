 * Definición de clases en PHP, que son de tres tipos:
 *   1. Clases abstractas:
 *      - No se pueden instanciar directamente. Sirven como base
          para que otras clases la extiendan.
 *
 *   2. Clases regulares:
 *      - Esta clase se puede instanciar directamente, pero también
 *        puede ser extendida por otras clases.
 *        
 *   3. Clases finales:
 *      - Solo permite instanciarse directamente; no puede ser extendida.
 *
 * Tome en cuenta que las clases en PHP o cualquier otro lenguaje de
 * programación son plantillas que le darán forma a un objeto; plantillas 
 * que permiten crear objetos con métodos y propiedades listos para usar.
 * 
